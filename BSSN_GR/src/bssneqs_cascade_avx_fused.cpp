    const VEC _zero = VSET(0.0);
    for (int i = 0; i < 3; i++)
        for (int j = 0; j < 3; j++)
            for (int k = 0; k < 3; k++) {
                VEC t_ij = (i==j) ? d_ch[k] : _zero;
                VEC t_ik = (i==k) ? d_ch[j] : _zero;
                VEC corr = VSUB(VADD(t_ij, t_ik), VMUL(gt[j][k], igt_dchi[i]));
                C3[i][j][k] = VFMA(nhalf_chi_inv, corr, C2[i][j][k]);
            }
    
